#pragma once

const uint8_t timer_program[] = {
  // SOD high
  0x00, MVI_A,
  0x01, 0b11000000,
  0x02, SIM,

  // set timer MSB
  0x10, MVI_H,
  0x11, 0x01,
  0x12, MVI_L,
  0x13, 0x05,
  0x14, MVI_M,
  0x15, 0x80 | 0x3f, // MSB

  // set timer LSB
  0x16, MVI_H,
  0x17, 0x01,
  0x18, MVI_L,
  0x19, 0x04,
  0x1a, MVI_M,
  0x1b, 0x00, // LSB

  // enable timer
  0x1c, MVI_H,
  0x1d, 0x01,
  0x1e, MVI_L,
  0x1f, 0x00,
  0x20, MVI_M,
  0x21, 0xc0,

  0x22, HLT,

  // SOD low
  TRAP+0, MVI_A,
  TRAP+1, 0b01000000,
  TRAP+2, SIM,
  TRAP+3, HLT,
};
